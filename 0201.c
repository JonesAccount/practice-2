#include <stdlib.h>
#include <stdio.h>

int main(void) {
    
    char map[30][51] = {
        "##################################################",   
        "##################################################",
        "##################################################",
        "##################################################",   
        "##################################################",
        "##################################################",
        "##################################################",
        "################ @++++++##        ################",
        "################++++++++##   #  # ################",
        "################   ##+++##   #  # ################",
        "################   ##+++######  # ################",
        "################        ##      # ################",
        "################   ##           # ################",
        "################   ##   ######  # ################",
        "################        ######  # ################",
        "################   ##     #     # ################",
        "################   ##     #     # ################",
        "################            ##### ################",
        "##########################     ## ################",
        "##########################   #### ################",
        "################               ## ################",
        "################  ##########   ###################",
        "################           #    x ################",
        "##################################################",   
        "##################################################",
        "##################################################",
        "##################################################",
        "##################################################",   
        "##################################################",
        "##################################################",
    };
    
    for (int i = 0; i < 30; i++) {
        for (int j = 0; j < 50; j++) {
            if (map[i][j] == ' ') map[i][j] = '+';
        }
    }
    
    
    for (int i = 0; i < 30; i++) {
        for (int j = 0; j < 50; j++) {
            if (map[i][j] == '#')
                printf("\033[32m%c\033[0m", map[i][j]);
            else if (map[i][j] == '+')
                printf("\033[33m%c\033[0m", map[i][j]);
            else
                printf("%c", map[i][j]);
        }
        printf("\n");
    }
    
    
    return 0;
}
